const state = {
  products: [],
  site: {},
  language: localStorage.getItem("svetagem-language") || document.documentElement.lang || "en",
  filter: "__all__",
  search: "",
  cart: JSON.parse(localStorage.getItem("svetagem-cart") || "[]")
};

const elements = {
  products: document.querySelector("[data-products]"),
  filters: document.querySelector("[data-filters]"),
  search: document.querySelector("[data-search]"),
  languageSelect: document.querySelector("[data-language-select]"),
  cartCount: document.querySelector("[data-cart-count]"),
  cartPanel: document.querySelector("[data-cart-panel]"),
  cartItems: document.querySelector("[data-cart-items]"),
  requestForm: document.querySelector("[data-request-form]"),
  checklistField: document.querySelector("[data-checklist-field]"),
  formStatus: document.querySelector("[data-form-status]")
};

async function init() {
  try {
    const [productsResponse, siteResponse] = await Promise.all([
      fetch("data/products.json", { cache: "no-store" }),
      fetch("data/site.json", { cache: "no-store" })
    ]);

    if (!productsResponse.ok) throw new Error(`Could not load data/products.json: ${productsResponse.status}`);
    if (!siteResponse.ok) throw new Error(`Could not load data/site.json: ${siteResponse.status}`);

    state.products = await productsResponse.json();
    state.site = await siteResponse.json();
    state.language = supportedLanguages().includes(state.language) ? state.language : state.site.defaultLanguage || "en";

    bindEvents();
    applyTranslations();
    renderFilters();
    renderProducts();
    renderCart();
  } catch (error) {
    console.error(error);
    elements.products.innerHTML = `<p>${escapeHtml(t("load_error"))}</p>`;
  }
}

function supportedLanguages() {
  return state.site.supportedLanguages || ["en", "ru", "vi"];
}

function t(key) {
  return I18N[state.language]?.[key] || I18N.en[key] || key;
}

function translateProduct(product, field) {
  return product.translations?.[state.language]?.[field] || product.translations?.en?.[field] || product[field] || (field === "tags" ? [] : "");
}

function bindEvents() {
  elements.search.addEventListener("input", (event) => {
    state.search = event.target.value.toLowerCase();
    renderProducts();
  });

  elements.languageSelect.value = state.language;
  elements.languageSelect.addEventListener("change", (event) => {
    state.language = event.target.value;
    localStorage.setItem("svetagem-language", state.language);
    applyTranslations();
    renderFilters();
    renderProducts();
    renderCart();
  });

  document.querySelectorAll("[data-open-cart]").forEach((button) => {
    button.addEventListener("click", () => elements.cartPanel.classList.add("is-open"));
  });

  document.querySelectorAll("[data-close-cart]").forEach((button) => {
    button.addEventListener("click", () => elements.cartPanel.classList.remove("is-open"));
  });

  elements.requestForm.addEventListener("submit", submitRequest);
}

function applyTranslations() {
  document.documentElement.lang = state.language;
  elements.languageSelect.value = state.language;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    node.placeholder = t(node.dataset.i18nPlaceholder);
  });

  document.querySelectorAll("[data-i18n-content]").forEach((node) => {
    node.setAttribute("content", t(node.dataset.i18nContent));
  });
}

function renderFilters() {
  const categoryMap = new Map();
  state.products.forEach((product) => {
    const category = translateProduct(product, "category");
    if (category) categoryMap.set(category, category);
  });

  const categories = [["__all__", t("all")], ...Array.from(categoryMap, ([value, label]) => [value, label])];

  if (state.filter !== "__all__" && !categoryMap.has(state.filter)) {
    state.filter = "__all__";
  }

  elements.filters.innerHTML = categories
    .map(([value, label]) => `<button class="filter-button ${value === state.filter ? "is-active" : ""}" type="button" data-category="${escapeHtml(value)}">${escapeHtml(label)}</button>`)
    .join("");

  elements.filters.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => {
      state.filter = button.dataset.category;
      renderFilters();
      renderProducts();
    });
  });
}

function renderProducts() {
  const visibleProducts = state.products.filter((product) => {
    const name = translateProduct(product, "name");
    const description = translateProduct(product, "description");
    const category = translateProduct(product, "category");
    const tags = translateProduct(product, "tags");
    const matchesCategory = state.filter === "__all__" || category === state.filter;
    const text = `${name} ${description} ${category} ${(tags || []).join(" ")}`.toLowerCase();
    return matchesCategory && text.includes(state.search);
  });

  if (visibleProducts.length === 0) {
    elements.products.innerHTML = `<p>${escapeHtml(t("no_products"))}</p>`;
    return;
  }

  elements.products.innerHTML = visibleProducts
    .map((product) => {
      const name = translateProduct(product, "name");
      const category = translateProduct(product, "category");
      const description = translateProduct(product, "description");
      const statusLabel = product.status === "sold_out" ? t("sold_out") : t("available");
      const buttonLabel = product.status === "sold_out" ? t("sold_out") : t("add_to_checklist");
      const images = Array.isArray(product.images) && product.images.length ? product.images : [product.image];
      const mainImage = images[0] || "assets/products/sets/placeholder-product.svg";
      const thumbnails = images.length > 1
        ? `<div class="product-thumbnails">${images.map((image, index) => `
            <button type="button" class="thumbnail-button ${index === 0 ? "is-active" : ""}" data-gallery-product="${escapeAttribute(product.id)}" data-gallery-image="${escapeAttribute(image)}" aria-label="${escapeAttribute(name)} image ${index + 1}">
              <img src="${escapeAttribute(image)}" alt="" loading="lazy" />
            </button>`).join("")}</div>`
        : "";
      return `
        <article class="product-card">
          <div class="product-image">
            <img src="${escapeAttribute(mainImage)}" alt="${escapeAttribute(name)}" loading="lazy" data-main-image="${escapeAttribute(product.id)}" />
            ${thumbnails}
          </div>
          <div class="product-body">
            <div class="product-meta">
              <span>${escapeHtml(category)}</span>
              <span class="status ${product.status}">${escapeHtml(statusLabel)}</span>
            </div>
            <h3>${escapeHtml(name)}</h3>
            <p>${escapeHtml(description)}</p>
            <strong>${formatMoney(product.price, product.currency)}</strong>
            <button type="button" data-add-to-cart="${escapeAttribute(product.id)}" ${product.status === "sold_out" ? "disabled" : ""}>
              ${escapeHtml(buttonLabel)}
            </button>
          </div>
        </article>`;
    })
    .join("");

  elements.products.querySelectorAll("[data-add-to-cart]").forEach((button) => {
    button.addEventListener("click", () => addToCart(button.dataset.addToCart));
  });

  elements.products.querySelectorAll("[data-gallery-image]").forEach((button) => {
    button.addEventListener("click", () => {
      const productId = button.dataset.galleryProduct;
      const mainImage = elements.products.querySelector(`[data-main-image="${cssEscape(productId)}"]`);
      if (mainImage) mainImage.src = button.dataset.galleryImage;
      button.parentElement.querySelectorAll(".thumbnail-button").forEach((item) => item.classList.remove("is-active"));
      button.classList.add("is-active");
    });
  });
}

function addToCart(productId) {
  const product = state.products.find((item) => item.id === productId);
  const existing = state.cart.find((item) => item.id === productId);

  if (!product || product.status === "sold_out") return;

  if (existing) {
    existing.quantity += 1;
  } else {
    state.cart.push({ id: product.id, quantity: 1 });
  }

  saveCart();
  renderCart();
  elements.cartPanel.classList.add("is-open");
}

function removeFromCart(productId) {
  state.cart = state.cart.filter((item) => item.id !== productId);
  saveCart();
  renderCart();
}

function saveCart() {
  localStorage.setItem("svetagem-cart", JSON.stringify(state.cart));
}

function cartLine(item) {
  const product = state.products.find((productItem) => productItem.id === item.id);
  if (!product) return null;
  return {
    ...item,
    name: translateProduct(product, "name"),
    price: product.price,
    currency: product.currency
  };
}

function renderCart() {
  const count = state.cart.reduce((total, item) => total + item.quantity, 0);
  elements.cartCount.textContent = count;

  const lines = state.cart.map(cartLine).filter(Boolean);
  if (lines.length === 0) {
    elements.cartItems.innerHTML = `<p>${escapeHtml(t("empty_checklist"))}</p>`;
    return;
  }

  elements.cartItems.innerHTML = lines
    .map((item) => `
      <div class="cart-item">
        <strong>${escapeHtml(item.name)}</strong>
        <span>${item.quantity} × ${formatMoney(item.price, item.currency)}</span>
        <button type="button" data-remove="${escapeAttribute(item.id)}">${escapeHtml(t("remove"))}</button>
      </div>`)
    .join("");

  elements.cartItems.querySelectorAll("[data-remove]").forEach((button) => {
    button.addEventListener("click", () => removeFromCart(button.dataset.remove));
  });
}

async function submitRequest(event) {
  event.preventDefault();

  if (state.cart.length === 0) {
    elements.formStatus.textContent = t("add_first");
    return;
  }

  const formData = new FormData(elements.requestForm);
  const checklist = state.cart
    .map(cartLine)
    .filter(Boolean)
    .map((item) => `${item.name} x ${item.quantity} (${formatMoney(item.price, item.currency)})`)
    .join("\n");

  formData.set("checklist", checklist);
  formData.set("language", state.language);
  elements.checklistField.value = checklist;

  const message = [
    t("request_email_title"),
    "",
    `${t("request_name")}: ${formData.get("name")}`,
    `${t("request_contact")}: ${formData.get("contact")}`,
    `${t("request_destination")}: ${formData.get("destination")}`,
    `${t("request_note")}: ${formData.get("note") || "-"}`,
    `Language: ${state.language}`,
    "",
    `${t("request_checklist")}:`,
    checklist
  ].join("\n");

  if (state.site.formEndpoint) {
    try {
      elements.formStatus.textContent = t("sending");

      const response = await fetch(state.site.formEndpoint, {
        method: "POST",
        headers: { Accept: "application/json" },
        body: formData
      });

      if (response.ok) {
        state.cart = [];
        saveCart();
        renderCart();
        elements.requestForm.reset();
        elements.formStatus.textContent = t("sent");
        return;
      }

      elements.formStatus.textContent = t("form_failed");
    } catch (error) {
      console.error(error);
      elements.formStatus.textContent = t("form_unavailable");
    }
  } else {
    elements.formStatus.textContent = t("opening_email");
  }

  openMailFallback(message);
}

function openMailFallback(message) {
  const subjectText = t("request_subject");
  const subject = encodeURIComponent(subjectText);
  const body = encodeURIComponent(message);
  const to = encodeURIComponent(state.site.contactEmail);

  const url = state.site.mailProvider === "mailto"
    ? `mailto:${state.site.contactEmail}?subject=${subject}&body=${body}`
    : `https://mail.google.com/mail/?view=cm&fs=1&to=${to}&su=${subject}&body=${body}`;

  const opened = window.open(url, "_blank", "noopener,noreferrer");
  if (!opened) window.location.href = url;

  elements.formStatus.innerHTML = `${escapeHtml(t("email_opened_prefix"))} <a href="${escapeAttribute(url)}" target="_blank" rel="noopener noreferrer">${escapeHtml(t("email_opened_link"))}</a>.`;
}

function formatMoney(value, currency) {
  return new Intl.NumberFormat(state.language, { style: "currency", currency }).format(value);
}

function escapeHtml(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function escapeAttribute(value) {
  return escapeHtml(value);
}

function cssEscape(value) {
  if (window.CSS && CSS.escape) return CSS.escape(value);
  return String(value).replaceAll('"', '\"');
}

init();
